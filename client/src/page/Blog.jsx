import React, { useState } from 'react';
import './Blog.css';

const Blog = () => {
  const [comments, setComments] = useState([]);
  const [newComment, setNewComment] = useState('');
  const [email, setEmail] = useState('');
  const [subscribed, setSubscribed] = useState(false);

  const handleCommentSubmit = (e) => {
    e.preventDefault();
    if (newComment.trim()) {
      setComments([...comments, { text: newComment, date: new Date().toLocaleString() }]);
      setNewComment('');
    }
  };

  const handleSubscribe = (e) => {
    e.preventDefault();
    if (email.trim()) {
      setSubscribed(true);
      setEmail('');
    
    }
  };

  return (
    <div className='blog-container'>
      
      <article className='blog-article'>
        
        <h2>Статья 1: "Топ-5 трендов в дизайне ванных комнат на 2024 год"</h2>
        <div>
          <h3>Введение</h3>
          <p>Ванная комната — это не только функциональное пространство, но и место, где мы можем расслабиться и насладиться моментом. В этой статье мы рассмотрим самые актуальные тренды в дизайне ванных комнат на 2024 год, которые помогут вам создать стильное и комфортное пространство.</p>
        </div>
        <div>
          <h3>1. Минимализм</h3>
          <p>Минималистичный дизайн продолжает оставаться в моде. Чистые линии, простые формы и нейтральные цвета создают ощущение простора и спокойствия. Используйте встроенные шкафы и скрытые системы хранения, чтобы избежать беспорядка.</p>
        </div>
        <div>
          <h3>2. Натуральные материалы</h3>
          <p>Дерево, камень и другие натуральные материалы становятся все более популярными. Они добавляют тепла и уюта в ванную комнату. Рассмотрите возможность использования деревянных полок или каменной плитки для создания уникального стиля.</p>
        </div>
        <div>
          <h3>3. Умные технологии</h3>
          <p>Современные технологии проникают в каждую сферу нашей жизни, и ванные комнаты не исключение. Умные зеркала с подсветкой, термостаты для душа и системы управления освещением делают использование ванной комнаты более удобным и комфортным.</p>
        </div>
        <div>
          <h3>4. Яркие акценты</h3>
          <p>Не бойтесь добавлять яркие акценты в интерьер. Это могут быть цветные плитки, яркие полотенца или оригинальные аксессуары. Они помогут сделать пространство более живым и интересным.</p>
        </div>
        <div>
          <h3>5. Экологичность</h3>
          <p>С каждым годом все больше людей обращают внимание на экологические аспекты. Используйте водосберегающие сантехнические приборы и экологически чистые материалы, чтобы сделать вашу ванную комнату более устойчивой.</p>
        </div>
        <div>
          <h3>Заключение</h3>
          <p>Следуя этим трендам, вы сможете создать ванную комнату, которая будет не только красивой, но и функциональной. Не забывайте, что главное — это ваше удобство и комфорт.</p>
        </div>
        
      </article>

      
      <article className='blog-article'>
        <h2>Статья 2: "Как выбрать идеальную плитку для ванной комнаты"</h2>
        <div>
          <h3>Введение</h3>
          <p>Плитка — один из самых популярных материалов для отделки ванных комнат. Но как выбрать идеальную плитку, которая будет сочетать в себе красоту, долговечность и функциональность? В этой статье мы поделимся полезными советами.</p>
        </div>
        <div>
          <h3>1. Учитывайте влагостойкость</h3>
          <p>Ванная комната — это помещение с высокой влажностью, поэтому выбирайте плитку, которая устойчива к влаге. Керамическая и керамогранитная плитка — отличные варианты, так как они не впитывают воду и легко чистятся.</p>
        </div>
        <div>
          <h3>2. Размер плитки</h3>
          <p>Размер плитки может существенно повлиять на восприятие пространства. Большие плитки визуально увеличивают пространство, в то время как маленькие плитки могут добавить текстуры и интереса. Подумайте о том, какой эффект вы хотите достичь.</p>
        </div>
        <div>
          <h3>3. Цвет и текстура</h3>
          <p>Выбор цвета и текстуры плитки зависит от вашего стиля и предпочтений. Нейтральные цвета создают спокойную атмосферу, в то время как яркие и насыщенные оттенки могут добавить энергии. Текстурированная плитка может помочь избежать скольжения на мокром полу.</p>
        </div>
        <div>
          <h3>4. Укладка плитки</h3>
          <p>Правильная укладка плитки — это залог долговечности и эстетики. Если вы не уверены в своих силах, лучше обратиться к профессионалам. Они помогут избежать распространенных ошибок и обеспечат качественный результат.</p>
        </div>
        <div>
          <h3>Заключение</h3>
          <p>Выбор плитки для ванной комнаты — это важный шаг, который требует внимания к деталям. Учитывайте влагостойкость, размер, цвет и текстуру, чтобы создать идеальное пространство для себя.</p>
        </div>
      </article>

      {/* Секция комментариев */}
      <div className='comments-section'>
        <h3>Комментарии ({comments.length})</h3>
        <form onSubmit={handleCommentSubmit} className='comment-form'>
          <textarea
            value={newComment}
            onChange={(e) => setNewComment(e.target.value)}
            placeholder='Оставьте ваш комментарий...'
            required
          />
          <button type='submit'>Отправить</button>
        </form>
        <div className='comments-list'>
          {comments.map((comment, index) => (
            <div key={index} className='comment'>
              <p className='comment-text'>{comment.text}</p>
              <p className='comment-date'>{comment.date}</p>
            </div>
          ))}
        </div>
      </div>

      {/* Секция подписки */}
      <div className='subscription-section'>
        <h3>Подписаться на обновления</h3>
        {subscribed ? (
          <p className='success-message'>Спасибо за подписку!</p>
        ) : (
          <form onSubmit={handleSubscribe} className='subscribe-form'>
            <input
              type='email'
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              placeholder='Ваш email'
              required
            />
            <button type='submit'>Подписаться</button>
          </form>
        )}
      </div>
    </div>
  );
};

export default Blog;

